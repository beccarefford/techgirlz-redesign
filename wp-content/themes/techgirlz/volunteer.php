[/vc_column_text][/vc_tta_section][vc_tta_section i_icon_fontawesome="fa fa-user" title="Finding &amp; Choosing An Instructor" tab_id="1453408885023-4d73d835-7a14" add_icon="true"][vc_column_text]There are many places to find subject matter experts in a specific field and the best way for these purposes is probably word of mouth. However, if you are not sure where to start looking for an instructor, try these resources.

<strong>Local Schools</strong>: Colleges and universities often have outreach programs for the local community and/or a technology organization or department. Teaching is a great way for students to reinforce what they are learning, get some practical experience, and give back to their community. The faculty might also be interested in teaching a class.

<strong>Local Technology Companies</strong>: Many technology companies want to contribute to their community, especially if it means training the next generation of technologists. Use resources such as chamber of commerce or better business bureau in addition to word of mouth references.

<strong>Girl Develop It</strong> is an international organization that teaches technology topics to adult women. They have experienced teachers in a variety of technical subjects and are particularly sensitive to the issues surrounding women in technology.

<strong>Girl Geek Dinners</strong> is an international group that hosts events for women in technology. They do not focus on teaching but have access to experienced technologists and often work with local schools.

<strong>Meetup</strong> is a website that many technology groups use to post events. Search for a technology group that is active in your area then ask if they have members who would be interested in teaching.

<strong>Conferences</strong>: Many professional technology organizations participate in or host local conferences about specific subjects. Find these events and contact the organizers or sponsors to recruit teachers.

<strong>Search Engine</strong>: A simple way to start is by entering the topic and your area in a search engine. Sift through the results to find professional organizations or local meetings about the topic and contact them to recruit a teacher.

<strong>Advice to Instructors</strong>
Teaching technology to kids can be a daunting task, especially if you have never done it before. Here is some advice to inspire both technologists who are new to teaching and teachers who are new to the technology they are teaching.

<strong>Advice for Technologists New to Teaching
</strong>If you can teach technology to an adult then you have many of the skills to teach a kid. The key is to give them the tools to experiment with. Abstract concepts and theories are difficult for kids to learn, understand, or remember and as a result come across as boring. Keep the workshop plan focused on fun activities with concrete objectives and room to achieve results beyond the objective so that the kids will have fun, understand the material, and hopefully even progress beyond the workshop plan. Most importantly, you should have fun too! If you are able to communicate your excitement about the topic children are more likely to want to learn about it and listen to you. It's ok to be goofy and cheesy but it's not ok to assume you know better than the kids. Be open to their ideas, positive about their contributions to the discussion, and you might be surprised at what you can learn from them too.

<strong>Advice for Teachers New to Technology
</strong>You can never know everything about a topic. This is the most important concept to remember when teaching technology. Most people don't know as much as you think about the technology they use, even on a daily basis, which is why most websites have a help and search tool. Also remember that just because you are teaching a topic does not mean you are an expert in it. It just means that you know more about it than the other people in the room. It’s okay to say, “I don’t know”, “let’s try to find out” and “that’s a good question”.

If there is a person who challenges your knowledge or knows more than you do about a topic, use it as an opportunity to investigate the topic as a group - teaching is as much about learning for the teacher as it is for the students. Many great resources exist for people to learn about the TechShopz in a Box topics in the form of free online courses that you can take before teaching the subject. For quick syntax questions, use your favorite search engine. If you can help a few people learn a little bit more about a topic, just enough to get interested to learn more, then you have been successful.[/vc_column_text][/vc_tta_section][vc_tta_section i_icon_fontawesome="fa fa-users" title="Tips on Recruiting Attendees" tab_id="1453409046824-a8e9895a-cf24" add_icon="true"][vc_column_text]You can list your event in a Google directory. Google has launched this directory as part of Made with Code—an initiative to inspire millions of girls to experience the power of code.

Perhaps you have a small group of girls and want to supplement it with more girls for the event you are holding. For the most part, middle school aged girls are the ones who know where OTHER middle school aged girls are! So use word of mouth to entice their peers (neighbors, school friends, sports buddies, dance/drama buddies) to come to the event. Undoubtedly girls “in the know” will use social media to spread the word by text, tweet, or Instagram (the tween trifecta!). Just be sure to include a reputable website or email address for more information/registration so they know all of the pertinent details.

